"""Generate the Card 6 demo comparison artifact.

This is a deterministic, fully-offline demonstration of the baseline vs
best-of-8 comparison pipeline. It uses StubProvider so the artifact in this
folder can be regenerated byte-for-byte on any developer machine without the
4090 cortex being up. Real Track C runs against the cortex emit the same
artifact shape; this folder is here so PR reviewers can see exactly what the
deliverable looks like.

Run:
    python bench/results/trackc_bo8_demo_2026-06-24/_generate_demo.py
"""
from __future__ import annotations

from pathlib import Path

from bench.eval.base import Sampling
from bench.eval.bestofn import BestOfNRunner
from bench.eval.comparison import build_comparison, write_comparison_artifacts
from bench.eval.provider import StubProvider
from bench.eval.runner import BenchRunner
from bench.eval.tasks import get_task


RESULTS_DIR = Path(__file__).resolve().parent


def _solution_for(prompt: str) -> str:
    if "def add" in prompt:
        return "```python\ndef add(a, b):\n    return a + b\n```"
    if "def is_even" in prompt:
        return "```python\ndef is_even(n):\n    return n % 2 == 0\n```"
    if "def reverse_string" in prompt:
        return "```python\ndef reverse_string(s):\n    return s[::-1]\n```"
    return "```python\nraise NotImplementedError\n```"


def _wrong(_prompt: str) -> str:
    return "I do not know the answer"


def _bestofn_canned(prompt: str, sampling: Sampling) -> str:
    # First 4 of every 8 candidates are wrong; the rest are the correct
    # implementation. With n=8 the verifier always finds at least one passing
    # candidate, so best-of-8 mean is 1.0.
    cand_idx = sampling.seed % 8
    if cand_idx < 4:
        return _wrong(prompt)
    return _solution_for(prompt)


def main() -> None:
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)

    baseline_runner = BenchRunner(
        provider=StubProvider(canned=_wrong("")),
        run_label="trackc_bo8_demo",
        results_dir=RESULTS_DIR,
        temperature=0.0,
        n_bootstrap_resamples=1000,
    )
    baseline_summary = baseline_runner.run_task(
        get_task("code_pub"), seeds=[0, 1, 2]
    )

    bo_runner = BestOfNRunner(
        provider=StubProvider(canned_fn=_bestofn_canned),
        run_label="trackc_bo8_demo",
        results_dir=RESULTS_DIR,
        n_candidates=8,
        temperature=0.8,
        n_bootstrap_resamples=1000,
    )
    bo_summary = bo_runner.run_task(get_task("code_pub"), seeds=[0, 1, 2])

    comp = build_comparison(
        baseline_summary,
        bo_summary,
        notes=(
            "Demo run: StubProvider, code_pub skeleton dataset (3 problems). "
            "Real cortex runs against the 4090 produce the same artifact "
            "shape with the actual base-model numbers. See "
            "docs/sprints/SPRINT_3d_BENCH_PLAN_2026-06-21.md."
        ),
    )
    paths = write_comparison_artifacts(comp, RESULTS_DIR)
    print(f"baseline mean: {baseline_summary.metrics[baseline_summary.primary_metric]['mean']}")
    print(f"best-of-8 mean: {bo_summary.metrics[bo_summary.primary_metric]['mean']}")
    print(f"lift: {comp.lift_abs_pp:+.2f} pp")
    print(f"non-overlapping CI: {comp.non_overlapping_ci}")
    print(f"chart: {paths['svg']}")


if __name__ == "__main__":
    main()
